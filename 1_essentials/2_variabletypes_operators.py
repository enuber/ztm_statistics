# variable types
# Intergers - whole numbers no quotes used
# Floats - decimal point numbers no quotes used
# Strings - any word or text enclosed by quotes
# Boolean - True / False

# check variable type
name = 'Erik'
age = 50
print(type(name))  # string
print(type(age))  # int

height = 1.83
height_int = int(height)
print(height)  # 1.83
print(height_int)  # 1

# transform int to float
a = 1
b = float(a)

print(b)  # 1.0

# transform float/int to string

c = 3.245
d = str(c)
print(d)  # '3.245'


